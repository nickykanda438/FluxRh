    /**
     * Remove the specified resource from storage.
     */
    public function destroy(string $id)
    {
        // dd($id);
        $userId = User::findOrFail($id);
        $userId->delete();
        return redirect()->route('dashboard')->with('success', 'L\'utilisateur a été supprimé avec succès.');
    }
